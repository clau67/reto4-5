    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        //Capturamos lo valores que igresa el usuario
        String username = txtUsername.getText();
        String password = txtPassword.getText();

        //2Mostramos lo valore por consola44
        System.out.println("Username: " + username + ", password: " + password);
        //3.Validamos que los campos no esten vacios
        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Hay campos sin diligenciar", "Information", JOptionPane.WARNING_MESSAGE);
        } else {
            if (username.equals("MisionTIC") && password.equals("Ciclo2")) {
                JOptionPane.showMessageDialog(null, "Bienvenido al sistema Mision TIC");
                //Hacemos viible el nuevo JFrame de navegación
                UserMenu usermenu = new UserMenu();
                usermenu.setVisible(rootPaneCheckingEnabled);
                //Eliminamos el frame de logn para que no se muestre mas 
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Error al inciar sesión", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnLoginActionPerformed

